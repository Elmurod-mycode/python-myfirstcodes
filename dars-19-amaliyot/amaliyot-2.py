# Foydalanuvchidan son olib, uning kvadrati va kubini konsolga chiqaruvchi funksiya yozing.
def kvadrat_kubni_hisobla(son):
    """Foydalanuvchidan son qiymat qabul qilib olib,
     uning kvadrati va kubuni hisoblab beradigan funksiya."""
    print(f"{son} ning kvadradi {son**2} ga teng.\n"
          f"{son} ning kubi {son**3} ga teng.")
kvadrat_kubni_hisobla(23)